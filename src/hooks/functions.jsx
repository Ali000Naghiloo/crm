export const handleCopy = (value) => {
  if (navigator) {
    navigator.clipboard
      .writeText(value)
      .then(() => {
        toast.success("با موفقیت کپی شد");
      })
      .catch(() => {
        toast.error("کپی کردن با خطا مواجه شد");
      });
  }
};

