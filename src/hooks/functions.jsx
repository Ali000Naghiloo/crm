export function convertTimeFormat(isoString) {
  const date = new Date(isoString);
  const year = date.getFullYear();
  const month = date.getMonth() + 1; // Months are zero-indexed
  const day = date.getDate();

  return `${year}/${month}/${day}`;
}

export const convertDateToISO = (jalaliDate) => {
  const jalaliMoment = moment(jalaliDate, "jYYYY/jMM/jDD"); // Use 'jYYYY/jMM/jDD' to parse as Jalali

  const formattedDate = jalaliMoment.format("YYYY-MM-DDTHH:mm:ss");

  return `${formattedDate}Z`;
};

export const convertISOToDate = (isoDate) => {
  const gregorianMoment = moment(isoDate);

  const jalaliDate = gregorianMoment.format("jYYYY/jMM/jDD");

  return jalaliDate;
};
